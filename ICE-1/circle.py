import math
# area and permiter of circle
radius = input("please provide radius for a circle: ")
r = int(radius)
# calculating area of a circle
area = math.pi * r**2
# calculating perimeter of a circle
perimeter = 2 * math.pi * r
# printing area of a circle
print("Area of a circle is ", area)
# printing perimeter of a circle
print("Perimeter of a circle is ", perimeter)